#include "libft.h"

int	ft_countw(char const *s, char c)
{
	int		i;
	int		cw;

	i = 0;
	cw = 0;
	while (s[i])
	{
		while (s[i] != '\0' && s[i] == c)
			i ++;
		if (s[i] != c && s[i])
			cw ++;
		while (s[i] != '\0' && s[i] != c)
			i ++;
	}
	return (cw);
}
